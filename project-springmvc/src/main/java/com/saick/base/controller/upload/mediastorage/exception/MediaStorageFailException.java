package com.saick.base.controller.upload.mediastorage.exception;

@SuppressWarnings("serial")
public class MediaStorageFailException extends Exception {

	public MediaStorageFailException() {
		super();
	}

	public MediaStorageFailException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MediaStorageFailException(String arg0) {
		super(arg0);
	}

	public MediaStorageFailException(Throwable arg0) {
		super(arg0);
	}

}
